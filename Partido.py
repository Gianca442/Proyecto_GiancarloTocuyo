class Partido:
    def __init__(self, id, number, home, away, code, name, stadium_id, date):
        self.id = id
        self.number = number
        self.home = home
        self.away= away
        self.code = code
        self.name = name
        self.stadium_id = stadium_id
        self.date = date

def __str__(self):
        return f" Id: {self.id}, Numero: {self.number}, Local: {self.home}, Visitante: {self.away}, Codigo: {self.code}, Nombre: { self.name}, id del Estadio: {self.stadium_id}, Fecha: {self.date}"
       