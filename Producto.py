class Producto:
    def __init__(self, name, quantity, price, adiicional, stock):
        self.name = name
        self.quantity = quantity
        self.price= price
        self.adiicional = adiicional
        self.stock = stock
def __str__(self):
        return f"Nombre: {self.nombre}, Precio: {self.price}, Adicional: {self.adicional}, Productos: {self.stock}"
